#include "libft.h"

int	ft_atoi(const char *n)
{
	int	i;
	int	j;
	int	nb;

	i = 0;
	j = 1;
	while ((n[i] >= 9 && n[i] <= 13) || n[i] == 32)
		i++;
	if (n[i] == '-' || n[i] == '+')
	{
		if (n[i] == '-')
			j = -1;
		i++;
	}
	nb = 0;
	while (n[i] != '\0')
	{
		if (ft_isdigit(n[i]) == 0)
			return (nb * j);
		nb = nb * 10;
		nb += (n[i] - 48);
		i++;
	}
	return (nb * j);
}
